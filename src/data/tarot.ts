// ── 3-card combined reading ───────────────────────────────────

export interface ThreeCardReading {
  title: string;
  text: string;
}

export function getThreeCardReading(
  past: TarotCard,
  present: TarotCard,
  future: TarotCard,
  pastReversed = false,
  presentReversed = false,
  futureReversed = false
): ThreeCardReading {
  // Determine themes
  const positiveCards = ["太陽", "世界", "星", "力", "恋人"];
  const isPastPos = positiveCards.includes(past.name) && !pastReversed;
  const isPresentPos = positiveCards.includes(present.name) && !presentReversed;
  const isFuturePos = positiveCards.includes(future.name) && !futureReversed;

  const allPositive = isPastPos && isPresentPos && isFuturePos;
  const futureBright = isFuturePos && !isPastPos;
  const challenge = !isFuturePos && isPastPos;

  const pastLabel = pastReversed ? `${past.name}（逆位置）` : past.name;
  const presentLabel = presentReversed ? `${present.name}（逆位置）` : present.name;
  const futureLabel = futureReversed ? `${future.name}（逆位置）` : future.name;

  if (allPositive) {
    return {
      title: "光の三重奏",
      text: `あなたが「${pastLabel}」の時代に築いた経験は、今まさに「${presentLabel}」という形で美しく花開いています。そして「${futureLabel}」が照らす先には、これまでの全ての歩みが昇華された、より大きなあなたが待っています。この三枚は稀な組み合わせです——過去・現在・未来が一本の光の糸で結ばれ、あなたを祝福しています。怖れることなく、今この流れに身を任せてください。それが、あなたに用意された道です。`,
    };
  }

  if (futureBright) {
    return {
      title: "夜明けへの旅路",
      text: `「${pastLabel}」の時代、あなたは暗闇の中でも種を撒き続けていました。そして今、「${presentLabel}」が示すように、その種はようやく芽吹き始めています。前の道のりが険しかったからこそ、「${futureLabel}」が告げる夜明けは格別に美しいはずです。あなたが経験してきた試練は全て、この瞬間のために必要だったのかもしれません。過去を糧に、今の自分を信じて、躊躇なく次の一歩を踏み出してください。`,
    };
  }

  if (challenge) {
    return {
      title: "成長の試練",
      text: `「${pastLabel}」が示すあなたの輝かしい過去は、今も確かにあなたの土台となっています。しかし「${presentLabel}」は今、あなたに立ち止まることを求めています——それは後退ではなく、深化のサインです。「${futureLabel}」の時代もまだ霧の中かもしれませんが、これは終わりではありません。過去の光を忘れず、現在の問いに真摯に向き合うとき、あなたはより深い知恵と強さを手に入れるでしょう。答えは、必ずあなたのもとへ来ます。`,
    };
  }

  // Default balanced reading
  return {
    title: "魂の三章",
    text: `「${pastLabel}」があなたに与えたものは、今この瞬間「${presentLabel}」として静かに息づいています。あなたは今、過去から受け継いだ力と、未来への問いの間に立っています。「${futureLabel}」が指し示す先は、今のあなたの選択によって形作られていく世界です。この三枚のカードが紡ぐ物語は、あなたの内なる知恵がすでに知っている答えを映しています。自分を信じて、心の声に耳を傾けてください——それがあなたにとっての最善の道です。`,
  };
}
