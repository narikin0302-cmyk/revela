import { Client } from "@notionhq/client";

const notion = new Client({ auth: process.env.NOTION_API_KEY });
const DB_ID = process.env.NOTION_BLOG_DB_ID;

function p(text) {
  return { object: "block", type: "paragraph", paragraph: { rich_text: [{ type: "text", text: { content: text } }] } };
}
function h2(text) {
  return { object: "block", type: "heading_2", heading_2: { rich_text: [{ type: "text", text: { content: text } }] } };
}
function h3(text) {
  return { object: "block", type: "heading_3", heading_3: { rich_text: [{ type: "text", text: { content: text } }] } };
}
function bullet(text) {
  return { object: "block", type: "bulleted_list_item", bulleted_list_item: { rich_text: [{ type: "text", text: { content: text } }] } };
}
function divider() {
  return { object: "block", type: "divider", divider: {} };
}
function quote(text) {
  return { object: "block", type: "quote", quote: { rich_text: [{ type: "text", text: { content: text } }] } };
}

async function createArticle({ title, slug, description, date, tags, blocks }) {
  const res = await notion.pages.create({
    parent: { database_id: DB_ID },
    properties: {
      Title: { title: [{ text: { content: title } }] },
      slug: { rich_text: [{ text: { content: slug } }] },
      description: { rich_text: [{ text: { content: description } }] },
      date: { date: { start: date } },
      tags: { multi_select: tags.map((name) => ({ name })) },
    },
    children: blocks,
  });
  console.log(`✅ 作成完了: ${title} (${res.id})`);
}

