function h(text) {
  return { object: "block", type: "heading_2", heading_2: { rich_text: [{ type: "text", text: { content: text } }] } };
}
function h3(text) {
  return { object: "block", type: "heading_3", heading_3: { rich_text: [{ type: "text", text: { content: text } }] } };
}
function p(text) {
  return { object: "block", type: "paragraph", paragraph: { rich_text: [{ type: "text", text: { content: text } }] } };
}
function ul(items) {
  return items.map(i => ({ object: "block", type: "bulleted_list_item", bulleted_list_item: { rich_text: [{ type: "text", text: { content: i } }] } }));
}

async function createArticle(title, slug, description, tags, categories, order, blocks) {
  const page = await notion.pages.create({
    parent: { database_id: DB_ID },
    properties: {
      Title: { title: [{ type: "text", text: { content: title } }] },
      slug: { rich_text: [{ type: "text", text: { content: slug } }] },
      description: { rich_text: [{ type: "text", text: { content: description } }] },
      date: { date: { start: "2026-03-29" } },
      tags: { multi_select: tags.map(n => ({ name: n })) },
      category: { multi_select: categories.map(n => ({ name: n })) },
      order: { number: order },
    },
    children: blocks,
  });
  console.log(`✓ Created: ${title}`);
  return page;
}

