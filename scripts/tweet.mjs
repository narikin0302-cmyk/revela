function getPostIndex() {
  // 今日の日付を種にしてローテーション
  const today = new Date();
  const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / 86400000);
  const hour = today.getHours();

  // 時間帯別コンテンツ切り替え
  const timeSlot = hour < 10 ? 0 : hour < 15 ? 1 : hour < 20 ? 2 : 3;

  return { dayOfYear, timeSlot };
}

async function tweet() {
  const { dayOfYear, timeSlot } = getPostIndex();

  let post;

  // タイムスロット別コンテンツ選択
  if (timeSlot === 0) {
    // 朝 → RPGクラス紹介
    post = RPG_CLASS_POSTS[dayOfYear % RPG_CLASS_POSTS.length];
  } else if (timeSlot === 1) {
    // 昼 → 問いかけ
    post = QUESTION_POSTS[dayOfYear % QUESTION_POSTS.length];
  } else if (timeSlot === 2) {
    // 夕方 → 3日で作った話
    post = STORY_POSTS[dayOfYear % STORY_POSTS.length];
  } else {
    // 夜 → RPGクラス紹介（別のクラス）
    post = RPG_CLASS_POSTS[(dayOfYear + 8) % RPG_CLASS_POSTS.length];
  }

  console.log("投稿内容:\n", post.text);

  try {
    const result = await client.v2.tweet(post.text);
    console.log("✅ 投稿成功:", result.data.id);
  } catch (err) {
    console.error("❌ 投稿失敗:", err);
    process.exit(1);
  }
}

tweet();
