async function createArticle(title, slug, description, tags, categories, order, blocks) {
  const page = await notion.pages.create({
    parent: { database_id: DB_ID },
    properties: {
      Title: { title: [{ type: "text", text: { content: title } }] },
      slug: { rich_text: [{ type: "text", text: { content: slug } }] },
      description: { rich_text: [{ type: "text", text: { content: description } }] },
      date: { date: { start: "2026-03-29" } },
      tags: { multi_select: tags.map(n => ({ name: n })) },
      category: { multi_select: categories.map(n => ({ name: n })) },
      order: { number: order },
    },
    children: blocks,
  });
  console.log(`✓ Created: ${title}`);
  return page.id;
}

